    private void btnConfirmMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnConfirmMouseClicked
        if (verificar()) {
            if (gravar()) {
                JOptionPane.showMessageDialog(null, "Cliente inserido com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "erro ao inserir o Cliente");
            }
        }
    }//GEN-LAST:event_btnConfirmMouseClicked

    public boolean verificar() {
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String telefone = txtTelefone.getText();

        if (!nome.matches("^[\\p{L} ]+$")) {//a-
            JOptionPane.showMessageDialog(null, "O campo 'Nome' possui formato inválido");
            return false;
        }

        if (!cpf.matches("^(\\d{3}\\.?\\d{3}\\.?\\d{3}-?\\d{2})$")) {
            JOptionPane.showMessageDialog(null, "cpf com formato invalido");
            return false;
        }

        if (!telefone.matches("^(\\(?\\d{2}\\)?\\s?)?(9\\d{4}|[2-9]\\d{3})-?\\d{4}$")) {
            JOptionPane.showMessageDialog(null, "telefone com formato invalido");
            return false;
        }
        return true;
    }

    public boolean gravar() {
        ClienteController clc = new ClienteController();
        Cliente cli = new Cliente();

        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String telefone = txtTelefone.getText();
        String endereco = txtEndereco.getText();

        cli.setNome(nome);
        cli.setCpf(cpf);
        cli.setTelefone(telefone);
        cli.setEndereco(endereco);

        if (clc.inserirCliente(cli)) {
            return true;
        } else {
            return false;
        }
    }

